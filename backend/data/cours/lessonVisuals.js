function pickLessonVisual(level, lessonNumber) {
  const normalizedLevel = String(level || '').toUpperCase()
  if (!LEVELS.includes(normalizedLevel)) return null
  const list = LESSON_VISUALS[normalizedLevel] || []
  if (list.length === 0) return null

  const indexRaw = Number(lessonNumber) || 1
  const index = ((indexRaw - 1) % list.length + list.length) % list.length
  const item = list[index]

  return {
    level: normalizedLevel,
    index: index + 1,
    themeFr: item.themeFr,
    url: item.url,
  }
}

